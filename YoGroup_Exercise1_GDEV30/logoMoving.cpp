// called by the main function to do rendering per frame
void render()
{
    // clear the whole frame
    glClearColor(1.0f, 1.0f, 1.0f, 1.0f);
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

    // compute a value for the glow amount for this frame
    float time = glfwGetTime();
    float speed = 2.0f;
    float glow = fabs(sin(time)/ 2.0f);

    // BACKGROUND SHADER
    glUseProgram(backgroundShader);

    glUniform1f(glGetUniformLocation(backgroundShader, "time"), time);
    glUniform1f(glGetUniformLocation(backgroundShader, "speed"), speed);

    glBindVertexArray(backgroundVao);
    glDrawArrays(GL_TRIANGLES, 0, sizeof(backgroundVertices) / (6 * sizeof(float)));

    // SHADOW SHADER
    glUseProgram(shadowShader);

    glUniform1f(glGetUniformLocation(shadowShader, "time"), time);
    glUniform1f(glGetUniformLocation(shadowShader, "speed"), speed);

    glBindVertexArray(shadowVao);
    glDrawArrays(GL_TRIANGLES, 0, sizeof(shadowVertices) / (6 * sizeof(float)));

    // KETTLE SHADER
    glUseProgram(kettleShader);

    glUniform1f(glGetUniformLocation(kettleShader, "time"), time);
    glUniform1f(glGetUniformLocation(kettleShader, "speed"), speed);

    glBindVertexArray(kettleVao);
    glDrawArrays(GL_TRIANGLES, 0, sizeof(kettleVertices) / (6 * sizeof(float)));

    // HOLE SHADER
    glUseProgram(holeShader);

    glUniform1f(glGetUniformLocation(holeShader, "time"), time);
    glUniform1f(glGetUniformLocation(holeShader, "speed"), speed);

    glBindVertexArray(holeVao);
    glDrawArrays(GL_TRIANGLES, 0, sizeof(holeVertices) / (6 * sizeof(float)));

    // EYE SHADER
    glUseProgram(eyeShader);

    glUniform1f(glGetUniformLocation(eyeShader, "time"), time);
    glUniform1f(glGetUniformLocation(eyeShader, "speed"), speed);
    glUniform1f(glGetUniformLocation(eyeShader, "glow"), glow);

    glBindVertexArray(eyeVao);
    glDrawArrays(GL_TRIANGLES, 0, sizeof(eyeVertices) / (6 * sizeof(float)));

    // LIGHTNING SHADER
    glUseProgram(lightningShader);

    glUniform1f(glGetUniformLocation(lightningShader, "time"), time);
    glUniform1f(glGetUniformLocation(lightningShader, "speed"), speed);

    glBindVertexArray(lightningVao);
    glDrawArrays(GL_TRIANGLES, 0, sizeof(lightningVertices) / (6 * sizeof(float)));
}

